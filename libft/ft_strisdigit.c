#include "libft.h"

int     ft_strisdigit(char *str)
{
    int     i;

    i = 0;
    while (str[i])
    {
        if (i == 0 && str[i] == '-')
            i++;
        else
        {
            if (!ft_isdigit(str[i]))
                return (0);
            i++;
        }
    }
    return (1);
}